"use strict"



let item1 = document.getElementById('item1');
let pizzaImg3 = document.getElementById('pizzaImg3');
let pizzaCrust = document.getElementById('pizzaCrust');

item1.addEventListener('change', function() {
    if (item1.checked) {
      pizzaImg3.style.display = "block";
      pizzaImg3.style.position = "absolute";
      pizzaCrust.style.position = "absolute";
    } else {
      pizzaImg3.style.display = "none";
      pizzaCrust.style.position = "absolute";
    }
  });

